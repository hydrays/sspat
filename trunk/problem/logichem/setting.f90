!!$ Stem cell simulation

!!$ >>> NSpec = 5

!!$ (1,  2,  3,  4,  5)
!!$  SC  TA  TC  MC  TMC

!!$ >>> NReac = 8

!!$  1: SC -> 2 SC
!!$  2: SC -> SC + TA
!!$  3: SC -> 2 TA
!!$  4: TA -> 2 TA
!!$  5: TA -> TA + TC
!!$  6: TA -> 2 TC
!!$  7: MC -> 2 MC
!!$  8: MC -> 2 TMC
!!$  9: TC -> 0
!!$  10: TMC -> 0
!!$  11: SC -> 0
!!$  12: TA -> 0
!!$  13: TC -> 0
!!$  14: MC -> 0
!!$  15: TMC -> 0
!!$  16: SC -> MC
!!$  17: TAC -> MC

module chem_data  
  use nrtype
  implicit none
  integer(I4B) :: NSample = 10000
  integer(I4B), parameter :: NSpec=15
  integer(I4B), parameter :: NReac=41
  real(kind=8) ap, p0, p1, v0, q1, q2, q3
  real(kind=8) vmut(6), pm(6)
  real(kind=8) k1, k2, v0max, v0min
  real(kind=8) qq1, qq2, qq3
  real(kind=8), parameter :: L = 200
  real(kind=8), parameter :: mu = 0.00001

  integer(I4B), parameter, dimension(NSpec,NReac) :: nu = reshape( &
       source=(/ &
! SC division
       (/01, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00/), & !1 
       (/00, 01, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00/), & !2
       (/-1, 02, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00/), & !3
! TAC division
       (/00, 01, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00/), & !4
       (/00, 00, 01, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00/), & !5
       (/00, -1, 02, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00/), & !6
! MC1 division
       (/00, 00, 00, 01, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00/), & !7
       (/00, 00, 00, -1, 02, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00/), & !8
! MC2 division
       (/00, 00, 00, 00, 00, 01, 00, 00, 00, 00, 00, 00, 00, 00, 00/), &
       (/00, 00, 00, 00, 00, -1, 02, 00, 00, 00, 00, 00, 00, 00, 00/), &
! MC3 division
       (/00, 00, 00, 00, 00, 00, 00, 01, 00, 00, 00, 00, 00, 00, 00/), &
       (/00, 00, 00, 00, 00, 00, 00, -1, 02, 00, 00, 00, 00, 00, 00/), &
! MC4 division
       (/00, 00, 00, 00, 00, 00, 00, 00, 00, 01, 00, 00, 00, 00, 00/), &
       (/00, 00, 00, 00, 00, 00, 00, 00, 00, -1, 02, 00, 00, 00, 00/), &
! MC5 division
       (/00, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00, 01, 00, 00, 00/), &
       (/00, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00, -1, 02, 00, 00/), &
! MC6 division
       (/00, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00, 01, 00/), &
       (/00, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00, -1, 02/), &

! TDC die
       (/00, 00, -1, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00/), & !9
       (/00, 00, 00, 00, -1, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00/), & !10
       (/00, 00, 00, 00, 00, 00, -1, 00, 00, 00, 00, 00, 00, 00, 00/), &
       (/00, 00, 00, 00, 00, 00, 00, 00, -1, 00, 00, 00, 00, 00, 00/), &
       (/00, 00, 00, 00, 00, 00, 00, 00, 00, 00, -1, 00, 00, 00, 00/), &
       (/00, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00, -1, 00, 00/), &
       (/00, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00, -1/), &

! All cell die
       (/-1, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00/), & !11
       (/00, -1, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00/), & !12
       (/00, 00, -1, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00/), & !13
       (/00, 00, 00, -1, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00/), & !14
       (/00, 00, 00, 00, -1, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00/), & !15

       (/00, 00, 00, 00, 00, -1, 00, 00, 00, 00, 00, 00, 00, 00, 00/), &
       (/00, 00, 00, 00, 00, 00, -1, 00, 00, 00, 00, 00, 00, 00, 00/), &
       (/00, 00, 00, 00, 00, 00, 00, -1, 00, 00, 00, 00, 00, 00, 00/), &
       (/00, 00, 00, 00, 00, 00, 00, 00, -1, 00, 00, 00, 00, 00, 00/), &
       (/00, 00, 00, 00, 00, 00, 00, 00, 00, -1, 00, 00, 00, 00, 00/), &
       (/00, 00, 00, 00, 00, 00, 00, 00, 00, 00, -1, 00, 00, 00, 00/), &
       (/00, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00, -1, 00, 00, 00/), &
       (/00, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00, -1, 00, 00/), &
       (/00, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00, -1, 00/), &
       (/00, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00, -1/), &

! mutation
       (/00, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00, 00/) &
       /), shape = (/NSpec, NReac/) &
       )

contains
  subroutine getrate(x, a)
    implicit none
    real(kind=8), intent(in) :: x(NSpec)
    real(kind=8), intent(out) :: a(NReac)
    real(kind=8) TGFbeta
    v0max = 3.0
    v0min = 0.5
    k1 = 1.0
    k2 = v0max/v0min - 1.0
    p1 = 0.4
    !TGFbeta = x(3) + x(5) + x(7) + x(9) + x(11) + x(13) + x(15)
    TGFbeta = x(3)
    p0 = 1.0/(1.01 + k1*TGFbeta/L)
    v0 = v0max/(1.0 + k2*TGFbeta/L)
    !v0 = 0.65

    if ( p0 .le. 0.5 ) then
       q2 = 2.0*p0
    else
       q2 = -2.0*(p0 - 1.0)
    end if
    q1 = p0 - q2*0.5
    q3 = 1.0 - q1 - q2

!!$    q1 = p0
!!$    q2 = 0.0
!!$    q3 = 1.0 - q1

    qq1 = 0.0
    qq2 = 2.0*p1
    qq3 = 1.0 - qq1 - qq2
!!$
!!$    qq1 = p1
!!$    qq2 = 0.0
!!$    qq3 = 1.0 - qq1 - qq2

    if (sum(x) > L) then
       ap = 0.1*(sum(x) - L)
    else
       ap = 0.0
    end if

! SC division
    a(1) = q1*v0*x(1)
    a(2) = q2*v0*x(1)
    a(3) = q3*v0*x(1)

! TAC division
    a(4) = qq1*x(2)
    a(5) = qq2*x(2)
    a(6) = qq3*x(2)

! MC division
    a(7) = vmut(1)*x(4)*pm(1)
    a(8) = vmut(1)*x(4)*(1.0-pm(1))

    a(9) = vmut(2)*x(6)*pm(2)
    a(10) = vmut(2)*x(6)*(1.0-pm(2))

    a(11) = vmut(3)*x(8)*pm(3)
    a(12) = vmut(3)*x(8)*(1.0-pm(3))

    a(13) = vmut(4)*x(10)*pm(4)
    a(14) = vmut(4)*x(10)*(1.0-pm(4))

    a(15) = vmut(5)*x(12)*pm(5)
    a(16) = vmut(5)*x(12)*(1.0-pm(5))

    a(17) = vmut(6)*x(14)*pm(6)
    a(18) = vmut(6)*x(14)*(1.0-pm(6))

! TDC die
    a(19) = 0.2*x(3)
    a(20) = 0.2*x(5)
    a(21) = 0.2*x(7)
    a(22) = 0.2*x(9)
    a(23) = 0.2*x(11)
    a(24) = 0.2*x(13)
    a(25) = 0.2*x(15)

! All cell die
    a(26) = ap*x(1)
    a(27) = ap*x(2)
    a(28) = ap*x(3)
    a(29) = ap*x(4)
    a(30) = ap*x(5)
    a(31) = ap*x(6)
    a(32) = ap*x(7)
    a(33) = ap*x(8)
    a(34) = ap*x(9)
    a(35) = ap*x(10)
    a(36) = ap*x(11)
    a(37) = ap*x(12)
    a(38) = ap*x(13)
    a(39) = ap*x(14)
    a(40) = ap*x(15)

    a(41) = mu*v0*x(1)+mu*x(2)

  end subroutine getrate

end module chem_data
